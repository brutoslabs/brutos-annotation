package org.brandao.brutos;

public class RenderViewException extends BrutosException{

	private static final long serialVersionUID = 4419561318336884296L;

	public RenderViewException() {
		super();
	}

	public RenderViewException(String message, Throwable cause) {
		super(message, cause);
	}

	public RenderViewException(String message) {
		super(message);
	}

	public RenderViewException(Throwable cause) {
		super(cause);
	}

}
