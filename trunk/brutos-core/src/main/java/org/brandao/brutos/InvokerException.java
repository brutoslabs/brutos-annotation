package org.brandao.brutos;

public class InvokerException 
	extends BrutosException{

	private static final long serialVersionUID = -528922331112903126L;

	public InvokerException() {
		super();
	}

	public InvokerException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvokerException(String message) {
		super(message);
	}

	public InvokerException(Throwable cause) {
		super(cause);
	}

}
