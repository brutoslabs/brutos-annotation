package org.brandao.brutos;

public class ActionResolverException extends BrutosException{

	private static final long serialVersionUID = -8302446266596131168L;

	public ActionResolverException() {
		super();
	}

	public ActionResolverException(String message, Throwable cause) {
		super(message, cause);
	}

	public ActionResolverException(String message) {
		super(message);
	}

	public ActionResolverException(Throwable cause) {
		super(cause);
	}

}
